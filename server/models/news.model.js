const mongoose = require('mongoose');

const NEWSSchema = new mongoose.Schema({
    user: {
        type: mongoose.Schema.Types.ObjectId,
        ref: 'User',
        required: true
    },
    title: { 
        type: String, 
        required: true 
    },
    description: [{ 
        type: String, 
        required: true 
    }],
    imageUrl: [{ 
        type: String, 
        required: true 
    }]
}, { timestamps: true });

const NEWS = mongoose.model('NEWS', NEWSSchema);

module.exports = NEWS;